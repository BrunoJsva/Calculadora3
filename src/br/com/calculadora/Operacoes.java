package br.com.calculadora;

public class Operacoes {
	
	public int soma (int num1, int num2) {
		int resultado;
		resultado = num1 + num2;
		return resultado;
	}
	
	public float subtracao (int num1, int num2) {
		int resultado;
		resultado = num1 - num2;
		return resultado;
	}
	
	public float divisao (int num1, int num2) {
		int resultado;
		resultado = num1 / num2;
		return resultado;
	}
	
	public float multiplicacao (int num1, int num2) {
		int resultado;
		resultado = num1 * num2;
		return resultado;
	}
	
	public float porcentagem (int num1, int num2) {
		int resultado;
		resultado = num1 % num2;
		return resultado;
	}
		
}
